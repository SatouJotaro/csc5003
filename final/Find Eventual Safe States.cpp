// 这道题给定一个有向图，要求找出所有"安全节点"——即从该节点出发的所有路径最终都能到达终止节点（出度为0的节点），不会陷入环中。
// 输出所有安全节点的升序列表。

// 最简逻辑：反向图 + 拓扑排序
// 核心思路： 把图反向，然后从所有终止节点开始做拓扑排序（类似 Kahn 算法）。原图中能"逃出环"的节点，在反向图中就是能被拓扑排序处理到的节点。

// 步骤：
// 建反向图，同时记录每个节点在原图中的出度
// 将所有出度为 0 的节点（终止节点）入队
// BFS：每处理一个节点，将反向图中它的前驱节点的出度减 1，若出度变为 0 则入队
// 所有被入队处理过的节点即为安全节点，排序输出

#include<bits/stdc++.h>
using namespace std;

int main(){
    int n; cin >> n;
    vector<vector<int>> rev(n);
    vector<int> outdeg(n, 0);
    for(int u = 0; u < n; u++){
        int d; cin >> d;
        outdeg[u] = d;
        for(int j = 0; j < d; j++){
            int v; cin >> v;
            rev[v].push_back(u);
        }
    }

    queue<int> q;
    for(int i = 0; i < n; i++){
        if(outdeg[i] == 0) q.push(i);
    }

    vector<bool> safe(n, false);
    while(!q.empty()){
        int u = q.front(); q.pop();
        safe[u] = true;
        for(int pre : rev[u]){
            outdeg[pre]--;
            if(outdeg[pre] == 0) q.push(pre);
        }
    }

    vector<int> ans;
    for(int i = 0; i < n; i++){
        if(safe[i]) ans.push_back(i);
    }

    cout << ans.size() << "\n";
    for(int i = 0; i < (int)ans.size(); i++){
        cout << ans[i] << " \n"[i + 1 == (int)ans.size()];
    }
    return 0;
}

// 拓扑排序 & Kahn算法
// 拓扑排序和 BFS 是不同概念，但 Kahn 算法用 BFS 来实现拓扑排序

// 拓扑排序是对有向无环图（DAG） 的节点进行线性排序，使得对于每条有向边 u → v，u 都排在 v 前面。 可以理解为"先修课"问题：学课 B 之前必须先学课 A，拓扑排序就给出一个合法的学习顺序。
// 拓扑排序只能用于无环图。如果图有环，排序无法完成。

// Kahn 算法的核心思想：每次找"入度为 0"的节点，把它输出，然后删掉它的边，再找下一个入度为 0 的节点。
// 入度 = 有多少条边指向这个节点。入度为 0 说明没有任何前置依赖，可以"第一个处理"。
// BFS 是"逐层扩展"，Kahn 也是用队列，每次从队列取一个节点、处理它的邻居、条件满足就入队——结构完全一样，只是入队条件从"未访问"换成了"入度变为0"。

// 这道安全节点题用的是反向图 + Kahn 思想，但操作的是出度而不是入度：本质是同一个 BFS 框架，只是"从终点反向传播"。能被这个 BFS 处理到的节点，说明它所有路径最终都能到达终止节点，不会卡在环里。

// safe数组
// safe 数组就是一个标记数组，用来记录"哪些节点最终确认是安全的"。
// 在代码里 vector<bool> safe(n, false) 初始化所有节点为"不安全"，然后每当一个节点从队列中被取出处理时，就把它标记为 true
// BFS 的起点是出度为 0 的终止节点（100% 安全）。之后通过反向图传播：只有当一个节点在原图中的所有出边对端都是安全的（出度归零），它才会入队。 换句话说，入队 = 确认安全，safe[u] = true 就是在记录这个"确认"的结果。
// BFS 结束后，遍历所有节点，把 safe[i] == true 的节点收集起来输出
// 因为 i 是从 0 到 n-1 顺序遍历的，天然就是升序，不需要额外排序。
// 简单说：safe 数组就是 BFS 的"访问记录"，只不过这里的访问条件不是普通的"是否走过"，而是"是否被确认为安全节点"。


