// n 个人（n ≤ 10），40 种帽子，每人有各自喜欢的帽子列表，要求每人分到恰好一顶自己喜欢的帽子且不重复，求合法分配方案数 mod 10^9 +7

// 做法：Bitmask DP（枚举帽子）
// 核心思路：用 dp[mask] 表示 mask 中的人都已分到帽子时的方案数。 
// 逐一枚举帽子 1..40，对每顶帽子，尝试把它分给喜欢它且还没分到帽子的某人。

// 为什么枚举帽子而非人？ 人数最多 10，bitmask 只有 2^10 = 1024 个状态；若用帽子做 bitmask 则高达，直接爆炸。

// 关键转移（对每顶帽子 h，对每个喜欢 h 的人 p）：
// 若 mask 中第 p 位未被占用：
//     dp[mask | (1 << p)] += dp[mask]

// 注意：每顶帽子循环时，mask 必须从大到小遍历，防止同一顶帽子被用两次（和0/1背包倒序同理）

#include <bits/stdc++.h>
using namespace std;

int main(){
    const int MOD = 1e9 + 7;
    int n; cin >> n;

    // hatToPeople[h] = 喜欢帽子h的人的列表
    vector<vector<int>> hatToPeople(41);
    for(int i = 0; i < n; i++){
        int k; cin >> k;
        while(k--){
            int h; cin >> h;
            hatToPeople[h].push_back(i);
        }
    }

    int full = (1 << n) - 1;
    vector<long long> dp(1 << n, 0);
    dp[0] = 1; // 0个人分配帽子，有1种方案（啥都不做）

    for(int h = 1; h <= 40; h++){
        // 从大到小枚举mask，避免同一顶帽子用两次
        for(int mask = full; mask >= 0; mask--){
            if(dp[mask] == 0) continue;
            for(int p : hatToPeople[h]){
                if(mask & (1 << p)) continue; // 第p个人已有帽子
                dp[mask | (1 << p)] = (dp[mask | (1 << p)] + dp[mask]) % MOD;
            }
        }
    }
    
    cout << dp[full] << "\n";
    return 0;
}


// full 和 (1 << n) - 1 是什么
// 这是 Bitmask（状态压缩） 的核心语法
// 核心思想：用一个整数的二进制位来表示一个集合——第 i 位为1表示第 i 个人已经分到帽子
// 1 << n 的意思：把 1 左移 n 位，即 2 ^ n
// 1 << 3 = 0b1000 = 8
// (1 << 3) - 1 = 0b0111 = 7   ← 恰好是 n=3 时"所有人都分到帽子"的状态

// | 操作        | 写法               | 含义       |
// | --------- | ---------------- | -------- |
// | 检查第p位是否为1 | mask & (1 << p)  | 人p是否已有帽子 |
// | 把第p位设为1   | mask | (1 << p) | 给人p分配帽子  |
// | 把第p位设为0   | mask & ~(1 << p) | 取消人p的帽子  |

// |（OR）：有1则1 → 用来置1（开关拨上）
// &（AND）：全1才1 → 用来检测或清零（检查开关/关掉开关）
// ~（NOT）：取反 → 配合 & 一起清零

// dp[mask] = mask 这个"人的集合"都分到帽子的方案数

// 初始：dp[0] = 1（没有人分配帽子时，有1种方案）

// 对每顶帽子 h（1..40）：
//   对每个已有状态 mask（从大到小，防止重复用帽子）：
//     对每个喜欢帽子 h 的人 p：
//       如果人 p 还没帽子（mask 的第p位为0）：
//         dp[mask | (1<<p)] += dp[mask]
//         ↑ 给人p戴帽子h，集合从mask扩展到包含p

// 答案：dp[full]（所有人都分到帽子的总方案数）
 




// 先理解 mask 的本质
// mask 是一个整数，但我们把它的二进制每一位当作一个开关
// mask 的二进制：  第2位  第1位  第0位
//                  人2    人1    人0
// mask = 0 = 0b000 → 没有人有帽子
// mask = 1 = 0b001 → 只有人0有帽子
// mask = 3 = 0b011 → 人0和人1有帽子
// mask = 7 = 0b111 → 人0、人1、人2全有帽子





// 三个位运算操作拆解
// ① 1 << p：生成"只有第p位是1"的数
// p = 0：1 << 0 = 0b001 = 1   （代表人0）
// p = 1：1 << 1 = 0b010 = 2   （代表人1）
// p = 2：1 << 2 = 0b100 = 4   （代表人2）


// ② mask & (1 << p)：检查人p是否已有帽子
// & 是"按位与"：两个位都是1才得1，否则得0
// mask = 0b011（人0和人1有帽子），检查人1（p=1）：
//   0b011
// & 0b010  ← (1 << 1)
// --------
//   0b010  ← 结果非0，说明人1已有帽子 ✓
// 同样检查人2（p=2）：
//   0b011
// & 0b100
// --------
//   0b000  ← 结果为0，说明人2还没有帽子 ✓
// 结论：mask & (1 << p) 非0 → 人p有帽子；为0 → 人p没帽子


// ③ mask | (1 << p)：给人p分配帽子（把第p位设为1）
// | 是"按位或"：有一个是1就得1。
// mask = 0b011（人0和人1有帽子），给人2（p=2）分配帽子：
//   0b011
// | 0b100  ← (1 << 2)
// --------
//   0b111  ← 人0、人1、人2都有帽子了 ✓
// 结论：mask | (1 << p) = 在原集合基础上，加入人p


// ④ mask & ~(1 << p)：撤销人p的帽子（把第p位设为0）
// ~ 是"按位取反"，& 再与上它就能清除那一位。（本题没用到，但备用）
// ~(1 << 1) = ~0b010 = 0b...11111101
// mask = 0b011
// & 0b...11111101
// = 0b001  ← 人1的帽子被撤销





