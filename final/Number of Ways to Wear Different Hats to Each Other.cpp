// 用样例来走一遍（h=3，只有人0喜欢帽子3）：
// 处理帽子3，hatToPeople[3] = {人0}

// 遍历到 mask = 0b000 = 0，dp[0] = 1（初始状态）：
//   检查人0：mask & (1<<0) = 0b000 & 0b001 = 0 → 人0没帽子，可以分
//   dp[0b000 | 0b001] += dp[0b000]
//   dp[0b001] += 1
//   → dp[1] = 1  （人0有帽子3，1种方案）

// 处理帽子4，hatToPeople[4] = {人0, 人1}

// 遍历到 mask = 0b001（人0已有帽子）：
//   检查人0：mask & (1<<0) = 0b001 ≠ 0 → 人0已有帽子，跳过
//   检查人1：mask & (1<<1) = 0b001 & 0b010 = 0 → 人1没帽子，可以分
//   dp[0b001 | 0b010] = dp[0b011] += dp[0b001] = 1
//   → dp[3] = 1  （人0有帽3，人1有帽4，1种方案）

// 处理帽子5，hatToPeople[5] = {人1, 人2}

// 遍历到 mask = 0b011（人0和人1已有帽子）：
//   检查人1：0b011 & 0b010 ≠ 0 → 跳过
//   检查人2：0b011 & 0b100 = 0 → 可以分
//   dp[0b011 | 0b100] = dp[0b111] += dp[0b011] = 1
//   → dp[7] = 1  ← 这就是dp[full]！

// 最终输出 dp[7] = 1 ✓




// 为什么 mask 要从大到小遍历？
// 和0/1背包从右往左原因完全一样：防止同一顶帽子在同一轮里被用两次

// 如果从小到大：
