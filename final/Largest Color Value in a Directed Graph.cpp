// 有向图中每个节点有一种颜色，路径的"颜色值"是该路径上出现最多的颜色节点数，求所有路径中最大颜色值；若图中有环则输出 -1

// 做法：拓扑排序 + DAG DP
// 核心思路：用 BFS 拓扑排序（Kahn算法）按顺序处理节点，维护 dp[u][c] = 以 u 结尾的路径中颜色 c 的最大出现次数
// 拓扑排序完后若处理的节点数不等于 n，说明有环返回 -1

// 转移（处理节点 u 后，对每条边 u→v）：
// dp[v][c] = max(dp[v][c], dp[u][c])   // 继承u的最优计数
// dp[v][color[u]] 已在处理u时+1         // u自身颜色计入

#include <bits/stdc++.h>
using namespace std;

int main() {
    int n, m; cin >> n >> m;
    string colors; cin >> colors;

    vector<vector<int>> g(n);
    vector<int> indegree(n, 0);
    for(int i = 0; i < m; i++){
        int a, b; cin >> a >> b;
        g[a].push_back(b);
        indegree[b]++;
    }

    // dp[u][c] = 以u结尾的路径中颜色c的最大出现次数
    vector<array<int, 26>> dp(n);
    for(auto& a : dp) a.fill(0);

    queue<int> q;
    for(int i = 0; i < n; i++){
        if(indegree[i] == 0) q.push(i);
    }

    int found = 0, ans = 0;
    while(!q.empty()){
        int u = q.front(); q.pop();
        found++;
        dp[u][colors[u] - 'a']++; // u自身颜色+1
        ans = max(ans, dp[u][colors[u] - 'a']);

        for(int v : g[u]){
            for(int c = 0; c < 26; c++){  // 将u的计数传递给v
                dp[v][c] = max(dp[v][c], dp[u][c]);
            }
            if(--indegree[v] == 0) q.push(v);
        }
    }

    cout << (found != n ? -1 : ans) << "\n";
    return 0;
}

// 节点：  0(a) → 1(b)
//               ↘
//         0(a) → 2(a) → 3(c) → 4(a)

// 拓扑序处理（入度为0的是节点0）

// | 处理节点    | 操作           | dp状态更新                                                  |
// | ------- | ------------ | ------------------------------------------------------- |
// | 0(a)    | a位+1         | dp[a]=1                                                 |
// | → 传给1,2 | 继承dp         | dplecture-9-dynamic-programming-2.pdf[a]=1, dpalgo[a]=1 |
// | 1(b)    | b位+1         | dplecture-9-dynamic-programming-2.pdf[b]=1，路径0→1        |
// | 2(a)    | a位+1         | dpalgo[a]=2，路径0→2 有2个a                                  |
// | → 传给3   | 继承dpalgo     | dpjointaro[a]=2                                         |
// | 3(c)    | c位+1         | dpjointaro[c]=1                                         |
// | → 传给4   | 继承dpjointaro | dphabr[a]=2                                             |
// | 4(a)    | a位+1         | dphabr[a]=3，路径0→2→3→4 有3个a                              |

// 为什么处理 u 时先给自身颜色 +1，再传给子节点？ 这样 v 继承的 dp[u] 已经包含了 u 自身的颜色贡献，路径 ...→u→v 的颜色计数就自然正确了

// "所有路径中，最大颜色值" 的意思是：
// 在有向图里，你可以走任意一条路径（从任意节点出发，沿有向边走到任意节点）。对于每条路径，数一数路径上出现最多的那种颜色的节点数，这就是这条路径的"颜色值"。最终要找所有路径中颜色值最大的那个。
// 样例：路径 0→2→3→4，字符分别是 a, a, c, a
//   颜色a出现3次，颜色c出现1次
//   这条路径的颜色值 = 3（最多的是a，出现3次）

// 路径 0→1，字符是 a, b，颜色值 = 1

// 所有路径中最大颜色值 = 3 ✓

// vector<array<int, 26>> dp(n) 为什么是 array？
// 因为每个节点需要记录 26种颜色 各自的计数，是一个固定大小26的小数组
// dp[u] 是一个长度26的数组：
// dp[u][0] = 以u结尾的路径中颜色'a'最多出现几次
// dp[u][1] = 以u结尾的路径中颜色'b'最多出现几次
// ...
// dp[u][25] = 以u结尾的路径中颜色'z'最多出现几次
// 如果写成 vector<vector<int>> dp(n, vector<int>(26, 0)) 也完全正确，效果一样，只是 array 更轻量（固定大小不需要动态分配内存）

// dp(n, 0) 只适用于一维：vector<int> dp(n, 0)，把 n 个整数全初始化为0。

// 但我们的 dp 是二维的（n个节点，每个节点有26个颜色计数），不能用一个0来初始化整个二维结构，所以要：
// vector<array<int, 26>> dp(n); // 先创建n个array
// for (auto& a : dp) a.fill(0); // 每个array里的26个元素全填0
// 等价写法（更直观）：
// vector<vector<int>> dp(n, vector<int>(26, 0)); // 也完全可以

// dp[u][colors[u] - 'a']++ 是在做什么？
// 这是把字符转换成0-25的数组下标的标准写法：
// colors[u] = 'a'  →  'a' - 'a' = 0  → dp[u][0]++  （颜色a计数+1）
// colors[u] = 'b'  →  'b' - 'a' = 1  → dp[u][1]++  （颜色b计数+1）
// colors[u] = 'c'  →  'c' - 'a' = 2  → dp[u][2]++  （颜色c计数+1）
// 字符之间做减法得到的是ASCII码的差值。因为小写字母在ASCII中是连续排列的（a=97, b=98, ...），所以 字符 - 'a' 就是该字母在字母表中的位置（0开始）。这句话的意思是：节点u自身的颜色，在dp中+1

// 整体逻辑：从入度为0的节点开始
// 入度 = 有多少条边指向这个节点。入度为0的节点没有任何"前驱"，即没有节点能到达它，所以它只能是路径的起点
// 拓扑排序（Kahn算法）的整体流程：

// 1. 计算所有节点的入度
// 2. 把所有入度为0的节点放入队列（这些是"起点"）
// 3. 每次从队列取出节点u：
//    - 给u自身颜色dp+1（u加入路径）
//    - 把dp[u]的所有颜色计数传递给u的每个邻居v（v继承u的最优状态）
//    - v的入度-1，如果变为0，说明v的所有前驱都处理完了，v也入队
// 4. 如果最终处理了n个节点 → 无环，输出答案
//    如果处理的节点 < n → 有环（有节点的入度永远不会变成0），输出-1

// 用一个比喻理解：就像流水线，只有所有"上游"工序完成后，"下游"工序才能开始。有环的话就是"甲等乙，乙等甲"，谁也无法开始，最终队列清空时发现还有节点没处理，即为有环