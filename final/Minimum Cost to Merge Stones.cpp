// 给 n 堆石子，每次合并恰好 k 堆连续的石子，代价是这 k 堆总石子数，求合并成1堆的最小总代价（不可能则返回 -1）

// 可行性：每次合并减少 k-1 堆，(n-1) % (k-1) != 0 时返回 -1 

// DP状态：dp[i][j] = 将 stones[i..j] 合并到尽可能少的堆的最小代价，分割点 mid 步长为 k-1 

// 转移：
// dp[i][j] = min(dp[i][mid] + dp[mid+1][j])，其中 mid = i, i+(k-1), i+2(k-1), ...
// 若 (j-i) % (k-1) == 0（可以合并成1堆），dp[i][j] += sum(i, j)

#include<bits/stdc++.h>
using namespace std;

int main(){
    int n, k; cin >> n >> k;
    vector<int> stones(n);
    for(int i = 0; i < n; i++) cin >> stones[i];

    // 不可能情况
    if((n - 1) % (k - 1) != 0) { cout << -1 << endl; return 0;}

    // 每次合并 k 堆变成 1 堆，堆数减少了 k-1
    // 要从 n 堆变成 1 堆，总共需要减少 n-1 堆
    // 而每次只能减少 k-1，所以 n-1 必须能被 k-1 整除

    // 前缀和，快速求区间和
    vector<int> pre(n+1, 0);
    for(int i = 0; i < n; i++) pre[i+1] = pre[i] + stones[i];

    // dp初始化为0（单堆代价为0）
    vector<vector<int>> dp(n, vector<int>(n, 0));
    // 下标是 0 ~ n-1，所以开 n×n 就够了
    // 对比编辑距离需要 (n+1)×(m+1) 是因为那道题需要"第0行第0列"表示空字符串，这道题不需要空区间，所以直接 n×n

    // 按区间长度枚举
    for(int len = 2; len <= n; len++){
        for(int i = 0; i + len - 1 < n; i++){
            int j = i + len - 1;
            dp[i][j] = INT_MAX;
            // 分割点步长为 k-1
            for(int mid = i; mid < j; mid += k - 1){
                if(dp[i][mid] != INT_MAX && dp[mid+1][j] != INT_MAX)
                    dp[i][j] = min(dp[i][j], dp[i][mid] + dp[mid+1][j]);
            }
            // 能合并成1堆时加上区间和
            if((j-i) % (k-1) == 0)
                dp[i][j] += pre[j+1] - pre[i];
        }
    }

    cout << dp[0][n - 1] << endl;
    return 0;
}

// "按区间长度枚举"在枚举什么？
// stones: [3, 2, 4, 1]
// 下标:    0  1  2  3

// 长度为1的区间（单堆，代价=0）：
//   [0,0]=3  [1,1]=2  [2,2]=4  [3,3]=1

// 长度为2的区间（尝试合并相邻2堆）：
//   [0,1]  [1,2]  [2,3]

// 长度为3的区间：
//   [0,2]  [1,3]

// 长度为4的区间（整个数组）：
//   [0,3]  ← 最终答案在这里

//   必须从小到大算，因为大区间要用小区间的结果 。这不是"枚举每两两合并到n个合并"，而是：先把所有小区间的最优解算出来，大区间直接查表。




// i + len - 1 是什么意思？
// len = 区间长度，i = 起点，j = 终点
// j = i + len - 1

// 比如 len=3, i=1：
//   j = 1 + 3 - 1 = 3
//   区间是 [1, 3]，包含下标 1,2,3，共3个元素

// 为什么不是 i + len？因为区间是闭区间，起点 i 本身也算1个元素，所以终点是 i + len - 1




// 为什么有"能合并成1堆"和"不能合并成1堆"的区别？
// 用 k=3 举例，看区间 [0,3]（4堆）：

// 4堆不能直接合并成1堆（需要3的倍数次合并），只能先拆成两部分各自合并

// 区间 [0,2]（3堆）能合并成1堆，合并时要加上这3堆的总石子数作为代价

// dp[i][j] 的含义：
//   若 (j-i) % (k-1) == 0 → 这个区间能缩成1堆，代价 = 内部合并代价 + 区间总和
//   否则 → 这个区间只能缩成若干堆，记录的是"到目前为止的合并代价"（不加区间和）

// 直觉上：每次最终合并成1堆时，才需要付出这次合并的"区间总和"代价，所以只有能合并成1堆的时候才 += sum(i,j)



