// 给定 n 根杆子，将它们分配给左、右两个支撑（或不用），要求两侧总长度相等，求最大可能的等高值

// 最简洁做法：差值DP（1D Bottom-Up）
// 核心思路：用 dp[d] 表示当两侧高度差为 d 时，较矮那侧的最大高度
// 初始 dp[0] = 0，其余为 -∞（不可达）
// 对每根杆子 x 从后往前更新（类似0/1背包），枚举三种选择
// 最终答案是 dp[0]

#include<bits/stdc++.h>
using namespace std;

int main(){
    int n; cin >> n;
    vector<int> a(n);
    int S = 0;  // S 是所有杆子的长度总和
    for(int i = 0; i < n; i++) {cin >> a[i]; S += a[i];}

    // dp[d] = 差值为d时，较矮侧的最大高度；-1表示不可达
    vector<int> dp(S + 1, -1);
    dp[0] = 0;
    
    for(int x : a){
        // 拷贝，避免本轮更新互相影响
        vector<int> ndp = dp;
        for(int d = 0; d <= S; d++){
            if(dp[d] < 0) continue;

            // 加到较高侧（差值增大）
            if(d + x <= S)
                ndp[d+x] = max(ndp[d+x], dp[d]);

            // 加到较矮侧
            int nd = abs(d-x);
            ndp[nd] = max(ndp[nd], dp[d] + min(d,x));
        }
        dp = ndp;
    }

    cout << (dp[0] < 0 ? 0 : dp[0]) << "\n";
    return 0;
}

// dp[d] 的定义是：两侧高度差为 d 时，较矮那侧能达到的最大高度
// 我们想要的是两侧等高，即差值 = 0。所以答案就是 dp[0]，它代表"两侧差值为0时，较矮侧（也就是两侧相等）的最大高度"


// 用一个具体例子来理解，假设当前状态是：左侧高度 = 5，右侧高度 = 3（差值 d = 2，较矮侧高度 h = 3），现在处理一根长度 x = 4 的杆子：

// 情况1：不用（skip）
// 状态不变，dp[2] 仍然可以是 3。

// 情况2：加到较高侧（左侧从 5 变成 9）
// 差值变成 9 - 3 = 6，较矮侧高度不变还是 3。
// ndp[d + x] = max(ndp[6], dp[2]) → ndp[6] = 3

// 情况3：加到较矮侧（右侧从 3 变成 7）
// 现在左 = 5，右 = 7，右侧更高了，差值 = |5 - 7| = 2，较矮侧 = 5。
// 较矮侧增加了多少？增加了 min(d, x) = min(2, 4) = 2，从3变成了5。
// ndp[abs(d - x)] = max(ndp[2], dp[2] + min(2, 4)) → ndp[2] = 5


// 为什么较矮侧增加的是 min(d, x) 而不是 x？ 这是最容易误解的地方。 分两个子情况：

// 若 x < d（杆子不够长补平差距）：右侧虽然加了 x，但依然比左侧矮。此时较矮侧（始终是右侧）高度增加了 x，即 min(d, x) = x。
// 若 x >= d（杆子补平甚至超过差距）：右侧变成了较高侧。此时新的较矮侧是原来的左侧（高度仍为 h + d）。较矮侧相比之前增加的是 d，即 min(d, x) = d

// if (dp[d] < 0) continue 是什么意思？
// 不是"找错杆"，而是"该状态不可达，跳过"。
// 初始化时 dp[0] = 0，其余全部为 -1（表示不可达）。如果 dp[d] < 0，说明目前还没有任何方式能让两侧高度差恰好为 d，这个状态根本不存在，自然不需要从它转移